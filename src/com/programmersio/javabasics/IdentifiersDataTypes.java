package com.programmersio.javabasics;

public class IdentifiersDataTypes {
    public static void main(String[] args) {
        // Boolean Data Type
        Boolean a =false;
        Boolean b = true;

        // Byte Data Type (-128 to 127)
        byte x = 10;

        // Short Data Type (-32768 to 32767)
        short s = 10000;

        // Int Data Type
        int y = 123456;

        // Long Data Type
        long l = 10000000L;

        // Float Data Type
        float fl = 234.5f;

        // Double Data Type
        double d = 12.345;

        // Char Data Type
        char charA = 'A';

        // Array
        int [] arr = new int[5];
        //int arr1[] = new int[5];- allowed but C type array declaration
        int[] arr3 = {1,2,3,4,5};
    }
;}
